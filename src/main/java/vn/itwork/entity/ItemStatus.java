package vn.itwork.entity;

public class ItemStatus {

	private int id;
	private String name;
	private String description;
	private String html_display;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getHtml_display() {
		return html_display;
	}
	public void setHtml_display(String html_display) {
		this.html_display = html_display;
	}
	
	
}
